package lekcijaPieci.majasDarbs;

public class Cilindrs {
    private int r;
    private int h;

    public Cilindrs(int r, int h) {
        this.r = r;
        this.h = h;
    }

    public Cilindrs() {
    }

    public int getR() {
        return r;
    }

    public void setR(int r) {
        this.r = r;
    }

    public int getH() {
        return h;
    }

    public void setH(int h) {
        this.h = h;
    }
}
